# Use of arithmatic operation use in python...

# Entered two number and it will give all arithmatic answers...

a=float(input('Enter number = '))
b=float(input('Enter number = '))
#c=a-b

print('Sum = {}'.format(a+b))
print('Sub = {}'.format(a-b))
print('Mul = {}'.format(a*b))
print('Reminder = {}'.format(a%b))  # Module or reminder after the division.
print('Div = {}'.format(a/b)) # Final answer after the division.
print('Floor Div = {}'.format(a//b))  # Quotient of the division.
print('power = {}'.format(a**b)) # Power to the number 